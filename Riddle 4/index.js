let años = parseInt(prompt("¿Cúantos años has vivido?"))
const MESES = 12
const SEMANAS = 52
const DIAS = 365
const HORAS = 8760
const MINUTOS = 525600
const SEGUNDOS = 31536000
alert(`Has vivido: ${años*MESES} meses, ${años*SEMANAS} semanas, ${años*DIAS} días, ${años*HORAS} horas, ${años*MINUTOS} minutos y ${años*SEGUNDOS} segundos`)